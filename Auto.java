@Autonomous (name="Auto", group="Pushbot")
public class Auto extends Movement {

    @Override
    public void start() {

    }

    @Override
    public void loop() throws InterruptedException{


        for (int i = 0; i < 4; i++){
            //Robot starts on the left of the field
            MoveForward(5000, 0.25);
            TurnRight(2000, 0.25);
            StrafeLeft(1000, 0.25);
            MoveBackward(1000, 0.25);

            telemetry.addData("Cycle %d complete", i+1);

        }

        telemetry.addData("Autonomous succeeded!", "Thank you for flying soccor bot airlines.");

    }
}